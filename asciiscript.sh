    # Install conda
    echo "Installing Miniconda..."
    brew install wget
    set +e
    wget -c https://repo.continuum.io/miniconda/Miniconda3-latest-MacOSX-x86_64.sh
    chmod +x Miniconda3-latest-MacOSX-x86_64.sh
    set -e
    ./Miniconda3-latest-MacOSX-x86_64.sh
